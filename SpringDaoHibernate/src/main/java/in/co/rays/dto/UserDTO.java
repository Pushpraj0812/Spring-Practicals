package in.co.rays.dto;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

import org.hibernate.annotations.GenericGenerator;

@Entity // creates table
@Table(name = "ST_USER") // table name
public class UserDTO {

	@Id // creates id
	@GeneratedValue(generator = "ncsPk") // @GeneratedValue-> generates id , (generator = "ncsPk")-> custom generator
	@GenericGenerator(name = "ncsPk", strategy = "native") // it generates id in sequence
	@Column(name = "ID", unique = true, nullable = false) // creates column name id, unique= true-> makes primary key,
															// nullable = false-> does not allow null value
	protected long id = 0;

	@Column(name = "FIRST_NAME", length = 50)
	private String firstName;

	@Column(name = "LAST_NAME", length = 50)
	private String lastName;

	@Column(name = "LOGIN", length = 50)
	private String login;

	@Column(name = "PASSWORD", length = 50)
	private String password;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}